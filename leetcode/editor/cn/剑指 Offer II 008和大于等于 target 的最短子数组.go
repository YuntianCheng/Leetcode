package main

import "fmt"

//leetcode submit region begin(Prohibit modification and deletion)
func minSubArrayLen(target int, nums []int) int {
	var i, j int
	var length = int(^uint(0) >> 1)
	var sum int
	for j < len(nums) || sum >= target {
		if sum >= target {
			if length > j-i {
				length = j - i
			}
			if i < len(nums) {
				sum -= nums[i]
				i++
			} else {
				break
			}
		} else if j < len(nums) {
			sum += nums[j]
			j++
		}
	}
	if length == int(^uint(0)>>1) {
		return 0
	}
	return length
}

