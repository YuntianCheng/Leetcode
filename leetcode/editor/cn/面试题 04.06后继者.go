/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

func inorderSuccessor(root *TreeNode, p *TreeNode) *TreeNode {
	var result *TreeNode
	stack := make([]*TreeNode, 0)
	cur := root
	top := -1
	var is bool
	for top >= 0 || cur != nil {
		for cur != nil {
			stack = append(stack, cur)
			top++
			cur = cur.Left
		}
		if is {
			result = stack[top]
			break
		}
		if stack[top] == p {
			is = true
		}
		cur = stack[top].Right
		stack = stack[:top]
		top--
	}
	return result
}

