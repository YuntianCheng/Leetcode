func reversePairs(nums []int) int {
	var cnt int
	for i := 1; i < len(nums); i++ {
		var j = i
		for ; j > 0; j-- {
			if nums[j] >= nums[j-1] {
				nums[j], nums[j-1] = nums[j-1], nums[j]
			} else {
				break
			}
		}
		cnt += j
	}
	return cnt
}

