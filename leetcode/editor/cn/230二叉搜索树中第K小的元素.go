/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

func kthSmallest(root *TreeNode, k int) int {
	stack := make([]*TreeNode, 0)
	top := -1
	count := 0
	for root != nil || top > -1 {
		if root != nil {
			if len(stack)-1 > top {
				top++
				stack[top] = root
			} else {
				stack = append(stack, root)
				top++
			}
			root = root.Left
		} else {
			root = stack[top]
			top--
			count++
			if count == k {
				return root.Val
			}
			root = root.Right
		}
	}
	return 0
}

